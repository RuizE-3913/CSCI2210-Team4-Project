    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        int row = tblFlights.getSelectedRow();

        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a flight!");
            return;
        }

        String flightNum = tblFlights.getValueAt(row, 0).toString();

        Flight selectedFlight = airline.getFlightManager().findFlight(flightNum);

        new SeatSelectionGUI(airline, selectedFlight).setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnSelectActionPerformed

