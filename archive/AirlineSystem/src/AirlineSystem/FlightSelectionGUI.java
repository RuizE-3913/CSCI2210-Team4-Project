    private void loadFlights() {

        FlightManager fm = airline.getFlightManager();

        String[] columns = {"Flight", "Origin", "Destination", "Price"};

        javax.swing.table.DefaultTableModel model =
            new javax.swing.table.DefaultTableModel(columns, 0);

        for (Flight f : fm.getAllFlights()) {
            model.addRow(new Object[]{
                f.getFlightNumber(),
                f.getOrigin().getAirportCode(),
                f.getDestination().getAirportCode(),
                f.getPrice()
                });
        }

        tblFlights.setModel(model);
}

